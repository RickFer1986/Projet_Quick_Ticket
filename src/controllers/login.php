<?php
// Verificar se o Login e senha estão corretos
loadModel('Login');

if(count($_POST) > 0) {
    $login = new Login($_POST);
    try {
        $user = $login->checkLogin();
        echo "Usuário {$user->name} logado :)";
    } catch(Exception $e) {
        echo 'Falha no login :(';
    }
}


loadView('login', $_POST);